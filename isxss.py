    def _pick_random_cookie_param(self, parameters):
        cookie_params = [p for p in parameters if p.getType() == IParameter.PARAM_COOKIE]
        if not cookie_params:
            return None

        # Always apply user-defined hard exclusions
        user_excluded = set()
        user_excluded_str = self.settings.get("excluded_cookies", "")
        if user_excluded_str:
            user_excluded = set(n.strip().lower() for n in user_excluded_str.split(",") if n.strip())

        eligible = [p for p in cookie_params if p.getName().lower() not in user_excluded]
        if not eligible:
            return None

        # When toggle is on, prefer non-session cookies within the eligible set
        if self.settings.get("prefer_non_session_cookies", True):
            common_session_names = set([
                "session", "sessionid", "phpsessid", "jsessionid", "asp.net_sessionid", "sid", "connect.sid"
            ])
            non_session = [p for p in eligible if p.getName().lower() not in common_session_names]
            if non_session:
                return random.choice(non_session)

        return random.choice(eligible)

    def _pick_random_header_name(self, headers):
        # Skip request line; only consider non-essential headers
        excluded = set([
            "host", "cookie", "content-length", "content-type", "transfer-encoding",
            "authorization", "proxy-authorization", "connection", "accept-encoding"
        ])
        candidates = []
        for h in headers[1:]:
            if ":" not in h:
                continue
            name = h.split(":", 1)[0].strip()
            if not name:
                continue
            if name.lower() in excluded:
                continue
            candidates.append(name)

        if not candidates:
            return None
        return random.choice(candidates)

    def _replace_header_value(self, request_bytes, header_name, new_value, http_service):
        if not header_name:
            return request_bytes

        try:
            req_info = self._helpers.analyzeRequest(http_service, request_bytes)
            headers = list(req_info.getHeaders())
            body_offset = req_info.getBodyOffset()
            body = request_bytes[body_offset:] if body_offset != -1 else None

            for i in range(1, len(headers)):
                if ":" not in headers[i]:
                    continue
                name, _ = headers[i].split(":", 1)
                if name.strip().lower() == header_name.lower():
                    headers[i] = name + ": " + new_value
                    break

            return self._helpers.buildHttpMessage(headers, body)
        except Exception:
            return request_bytes

    def _add_or_replace_header(self, request_bytes, header_name, new_value, http_service):
        """Add the header if not present in the request, otherwise replace its value."""
        if not header_name:
            return request_bytes
        try:
            req_info = self._helpers.analyzeRequest(http_service, request_bytes)
            headers = list(req_info.getHeaders())
            body_offset = req_info.getBodyOffset()
            body = request_bytes[body_offset:] if body_offset != -1 else None
            found = False
            for i in range(1, len(headers)):
                if ":" not in headers[i]:
                    continue
                name, _ = headers[i].split(":", 1)
                if name.strip().lower() == header_name.lower():
                    headers[i] = name + ": " + new_value
                    found = True
                    break
            if not found:
                headers.append(header_name + ": " + new_value)
            return self._helpers.buildHttpMessage(headers, body)
        except Exception:
            return request_bytes

    def _inject_payload_into_json(self, json_obj):
        """Recursively inject payload into all string values in a JSON structure."""
        if isinstance(json_obj, dict):
            return {k: self._inject_payload_into_json(v) for k, v in json_obj.items()}
        elif isinstance(json_obj, list):
            return [self._inject_payload_into_json(item) for item in json_obj]
        elif isinstance(json_obj, basestring):
            return self.payload
        else:
            return json_obj

    def _get_json_injected_value(self, param_value):
        """
        Detect if param_value (or its URL-decoded form) is a JSON object or array.
        If so, inject payload into all string values, re-serialize, and return
        the injected JSON string (unencoded). Returns None if not JSON.
        """
        try:
            decoded = unquote(param_value)
            # Try URL-decoded first, then raw (avoids duplicate attempt when no encoding)
            candidates = [decoded] if decoded != param_value else []
            candidates.append(param_value)
            for candidate in candidates:
                try:
                    json_obj = json.loads(candidate)
                    if not isinstance(json_obj, (dict, list)):
                        continue
                    injected = self._inject_payload_into_json(json_obj)
                    return json.dumps(injected, separators=(',', ':'))
                except ValueError:
                    continue
            return None
        except Exception:
            return None

    def _host_matches_scope(self, host):
        """Return True if host matches any of the comma-separated scope glob patterns,
        or if no scope filter is configured (allow all)."""
        scope_filter = self.settings.get("scope_filter", "").strip()
        if not scope_filter:
            return True
        patterns = [p.strip().lower() for p in scope_filter.split(",") if p.strip()]
        host_lower = host.lower()
        for pattern in patterns:
            if fnmatch.fnmatch(host_lower, pattern):
                return True
        return False

    def _get_base64_injected_value(self, param_value):
        """
        Detect if param_value (or its URL-decoded form) is base64-encoded text.
        If so, inject payload (using JSON injection if the decoded content is JSON,
        otherwise plain payload), re-encode to base64, and return the result.
        Returns None if not base64 or not useful text.
        """
        try:
            decoded_url = unquote(param_value)
            candidates = [decoded_url] if decoded_url != param_value else []
            candidates.append(param_value)
            for candidate in candidates:
                # Try standard then URL-safe base64
                for altchars in [None, b'-_']:
                    try:
                        padded = candidate + '=' * (-len(candidate) % 4)
                        if altchars:
                            raw = base64.b64decode(padded.replace('-', '+').replace('_', '/') + '=' * (-len(padded) % 4))
                        else:
                            raw = base64.b64decode(padded)
                        decoded_str = raw.decode('utf-8')
                        if not decoded_str.strip() or len(decoded_str) < 2:
                            continue
                        # Try JSON injection first, fall back to plain payload
                        json_injected = self._get_json_injected_value(decoded_str)
                        injected_str = json_injected if json_injected is not None else self.payload
                        re_encoded = base64.b64encode(injected_str.encode('utf-8'))
                        return re_encoded
                    except Exception:
                        continue
            return None
        except Exception:
            return None
    
    def _get_dynamic_path_segments(self, path):
        """
        Identify path segments that are likely dynamic (IDs, UUIDs, slugs with digits).
        Returns (all_segments, [(index, segment), ...]) where the second list contains
        only segments considered dynamic and safe to replace with the payload.
        """
        static_keywords = set([
            "api", "v1", "v2", "v3", "static", "assets", "admin", "auth",
            "login", "logout", "register", "signup", "search", "public",
            "private", "media", "images", "img", "js", "css", "fonts",
            "download", "upload", "export", "import", "graphql", "rest",
            "web", "app", "page", "pages", "home", "index", "about", "contact",
        ])
        segments = [s for s in path.split("/") if s]
        dynamic = []
        for i, seg in enumerate(segments):
            # Skip segments with file extensions (e.g. image.png, script.js)
            if "." in seg:
                continue
            seg_lower = seg.lower()
            if seg_lower in static_keywords:
                continue
            # Heuristic 1: pure numeric ID  (e.g. 12345)
            if re.match(r'^\d+$', seg):
                dynamic.append((i, seg))
                continue
            # Heuristic 2: UUID or long hex/token  (e.g. a1b2c3d4-... or 32+ hex chars)
            if re.match(r'^[0-9a-fA-F\-]{32,}$', seg):
                dynamic.append((i, seg))
                continue
            # Heuristic 3: slug that contains at least one digit (e.g. product-42, order_abc123)
            if re.match(r'^[a-zA-Z0-9_\-]{4,}$', seg) and re.search(r'\d', seg):
                dynamic.append((i, seg))
                continue
        return segments, dynamic

