    def processHttpMessage(self, toolFlag, messageIsRequest, messageInfo):
        # Check if message received is a request or response
        if(messageIsRequest):
            # Ignore requests originated by this extension (prevents redirect loops)
            try:
                req_bytes = messageInfo.getRequest()
                if hasattr(self, '_helpers') and self._helpers and req_bytes is not None:
                    req_str = self._helpers.bytesToString(req_bytes)
                    if req_str in self.our_requests:
                        # remove it to avoid unbounded growth and ignore processing
                        try:
                            self.our_requests.discard(req_str)
                        except Exception:
                            pass
                        return
            except Exception:
                pass
            try:
                request = messageInfo.getRequest()
                request_data = self._helpers.analyzeRequest(messageInfo.getHttpService(), request)
                headers = request_data.getHeaders()
                method = request_data.getMethod()

                # Respect settings
                if method == "POST" and not self.settings.get("scan_post", True):
                    return
            
                # Get the parameters from the request
                parameters = request_data.getParameters()
                
                # Avoid testing duplicate URLs - create unique key from URL and method
                url = request_data.getUrl()

                # Filter out requests to Google and Google Tag Manager hosts
                try:
                    host = messageInfo.getHttpService().getHost().lower()
                except Exception:
                    try:
                        host = url.getHost().lower()
                    except Exception:
                        host = ""

                # Ignore analytics, tracking, and ad-serving domains
                _ignored_suffixes = (
                    "google.com",
                    "googletagmanager.com",
                    "googleapis.com",
                    "google-analytics.com",
                    "googlesyndication.com",
                    "googleadservices.com",
                    "doubleclick.net",
                    "facebook.com",
                    "facebook.net",
                    "fbcdn.net",
                    "analytics.yahoo.com",
                    "bing.com",
                    "bat.bing.com",
                    "scorecardresearch.com",
                    "hotjar.com",
                    "clarity.ms",
                    "segment.com",
                    "segment.io",
                    "mixpanel.com",
                    "amplitude.com",
                    "fullstory.com",
                    "newrelic.com",
                    "nr-data.net",
                    "cloudflareinsights.com",
                    "cdn.cookielaw.org",
                    "onetrust.com",
                )
                if any(host == s or host.endswith("." + s) for s in _ignored_suffixes):
                    return
                if "googletagmanager" in host or "google-analytics" in host:
                    return

                # Scope filter
                if not self._host_matches_scope(host):
                    return

                if self.settings.get("path_level_dedup", True):
                    url_key = host + "|" + str(url.getPath()) + "|" + method
                else:
                    url_key = str(url) + "|" + method
                if url_key in self.tested_urls:
                    return
                self.tested_urls.add(url_key)

                selected_cookie_param = None
                if self.settings.get("inject_cookie", False):
                    selected_cookie_param = self._pick_random_cookie_param(parameters)

                selected_header_name = None
                if self.settings.get("inject_header", False):
                    selected_header_name = self._pick_random_header_name(headers)

                # Headers with high probability of being reflected into HTML response body.
                # Chosen because servers commonly echo these into page content:
                #   Referer       -> "back to" links, breadcrumbs, analytics displays
                #   User-Agent    -> browser/device detection messages
                #   Accept-Language -> locale selection, language banners
                #   X-Forwarded-For -> "your IP is..." or debug output
                #   X-Forwarded-Host -> affects host-based URL generation embedded in HTML
                #   X-Original-URL  -> URL rewriting that gets echoed back into page
                #   X-Rewrite-URL   -> same as above on IIS/nginx setups
                _KNOWN_REFLECTION_HEADERS = [
                    "Referer", "User-Agent", "Accept-Language",
                    "X-Forwarded-For", "X-Forwarded-Host",
                    "X-Original-URL", "X-Rewrite-URL"
                ]
                selected_known_header = None
                if self.settings.get("inject_known_headers", True):
                    selected_known_header = random.choice(_KNOWN_REFLECTION_HEADERS)

                new_parameters = []
                
                # Loop over the parameters and modify as necessary
                for parameter in parameters:
                    # Skip cookie parameters - they should not be injected into the URL/body
                    if parameter.getType() == IParameter.PARAM_COOKIE:
                        continue
                    if(self.payload in parameter.getValue()):
                        return
                    new_value = self.payload
                    new_parameter = self._helpers.buildParameter(parameter.getName(), new_value, parameter.getType())
                    new_parameters.append(new_parameter)

                if not new_parameters and not selected_cookie_param and not selected_header_name:
                    return

                requests_to_test = []
                
                # Handle GET requests
                if method == "GET":
                    # Helper: rebuild the request reusing original headers (preserves Cookie etc.)
                    # Only the first header (request line) is replaced with the new path+query.
                    def build_get_request(path_and_query):
                        new_headers = list(headers)
                        new_headers[0] = method + " " + path_and_query + " HTTP/1.1"
                        return self._helpers.buildHttpMessage(new_headers, None)

                    # Standard / per-parameter request(s)
                    if self.settings.get("per_param_testing", False) and len(new_parameters) > 1:
                        # Per-param mode: one request per param; other params keep original values
                        for target_p in new_parameters:
                            pp_parts = []
                            for orig_p in parameters:
                                if orig_p.getType() == IParameter.PARAM_COOKIE:
                                    continue
                                if orig_p.getName() == target_p.getName():
                                    pp_parts.append(orig_p.getName() + '=' + quote(self.payload, safe=''))
                                else:
                                    pp_parts.append(orig_p.getName() + '=' + quote(orig_p.getValue(), safe='='))
                            pp_path = url.getPath() + "?" + '&'.join(pp_parts)
                            if url.getRef():
                                pp_path += "#" + url.getRef()
                            requests_to_test.append(build_get_request(pp_path))
                    elif new_parameters:
                        # All-at-once mode (default)
                        new_query = '&'.join([p.getName() + '=' + quote(p.getValue(), safe='=') for p in new_parameters])
                        path_query = url.getPath() + "?" + new_query
                        if url.getRef():
                            path_query += "#" + url.getRef()
                        requests_to_test.append(build_get_request(path_query))
                    else:
                        # If only header/cookie mutation is enabled, still test the original request
                        requests_to_test.append(request)
                    
                    # Nested value requests: for params whose value contains '=',
                    # also test with only the sub-value replaced (e.g. setck=cplng=payload)
                    for orig_param in parameters:
                        if orig_param.getType() == IParameter.PARAM_COOKIE:
                            continue
                        orig_value = orig_param.getValue()
                        if '=' in orig_value:
                            eq_idx = orig_value.index('=')
                            nested_new_value = orig_value[:eq_idx + 1] + self.payload
                            variant_params = []
                            for p in new_parameters:
                                if p.getName() == orig_param.getName():
                                    variant_params.append(self._helpers.buildParameter(p.getName(), nested_new_value, p.getType()))
                                else:
                                    variant_params.append(p)
                            variant_query = '&'.join([p.getName() + '=' + quote(p.getValue(), safe='=') for p in variant_params])
                            variant_path_query = url.getPath() + "?" + variant_query
                            if url.getRef():
                                variant_path_query += "#" + url.getRef()
                            requests_to_test.append(build_get_request(variant_path_query))

                    # JSON value requests: for GET params whose (URL-decoded) value is JSON,
                    # inject the payload into all string values inside the JSON structure.
                    if new_parameters:
                        for orig_param in parameters:
                            if orig_param.getType() == IParameter.PARAM_COOKIE:
                                continue
                            json_injected = self._get_json_injected_value(orig_param.getValue())
                            if json_injected is not None:
                                variant_parts = []
                                for p in new_parameters:
                                    if p.getName() == orig_param.getName():
                                        variant_parts.append(p.getName() + '=' + quote(json_injected, safe=''))
                                    else:
                                        variant_parts.append(p.getName() + '=' + quote(p.getValue(), safe='='))
                                json_path_query = url.getPath() + "?" + '&'.join(variant_parts)
                                if url.getRef():
                                    json_path_query += "#" + url.getRef()
                                requests_to_test.append(build_get_request(json_path_query))

                    # Base64 value requests: for GET params that are base64-encoded,
                    # inject the payload, re-encode, and test.
                    if new_parameters and self.settings.get("inject_base64", True):
                        for orig_param in parameters:
                            if orig_param.getType() == IParameter.PARAM_COOKIE:
                                continue
                            b64_injected = self._get_base64_injected_value(orig_param.getValue())
                            if b64_injected is not None:
                                variant_parts = []
                                for p in new_parameters:
                                    if p.getName() == orig_param.getName():
                                        variant_parts.append(p.getName() + '=' + quote(b64_injected, safe=''))
                                    else:
                                        variant_parts.append(p.getName() + '=' + quote(p.getValue(), safe='='))
                                b64_path_query = url.getPath() + "?" + '&'.join(variant_parts)
                                if url.getRef():
                                    b64_path_query += "#" + url.getRef()
                                requests_to_test.append(build_get_request(b64_path_query))

                    # Fragment hash injection: some server-side renderers and frameworks
                    # reflect or parse the URL fragment. Test with payload in the fragment.
                    frag_base = url.getPath()
                    orig_query = url.getQuery()
                    if new_parameters:
                        frag_base += "?" + '&'.join([p.getName() + '=' + quote(p.getValue(), safe='=') for p in new_parameters])
                    elif orig_query:
                        frag_base += "?" + orig_query
                    existing_frag = url.getRef()
                    # Variant 1: replace entire fragment with payload.
                    # Only fire if the request already has a fragment, or the
                    # "inject into all fragments" setting is enabled.
                    if existing_frag is not None or self.settings.get("inject_all_fragments", False):
                        requests_to_test.append(build_get_request(frag_base + "#" + quote(self.payload, safe='')))
                    # Variant 2: if existing fragment has key=value pairs, inject into each value
                    if existing_frag and '=' in existing_frag:
                        frag_parts = existing_frag.split('&')
                        injected_frag_parts = []
                        for fpart in frag_parts:
                            if '=' in fpart:
                                fname = fpart.split('=', 1)[0]
                                injected_frag_parts.append(fname + '=' + quote(self.payload, safe=''))
                            else:
                                injected_frag_parts.append(fpart)
                        requests_to_test.append(build_get_request(frag_base + "#" + '&'.join(injected_frag_parts)))

                    # Double-encoded variants: inject %22/%3E/%3C so a WAF sees encoded chars
                    # while a double-decoding backend receives the plain payload.
                    if new_parameters and self.settings.get("double_encode", False):
                        for orig_param in parameters:
                            if orig_param.getType() == IParameter.PARAM_COOKIE:
                                continue
                            de_parts = []
                            for p in new_parameters:
                                if p.getName() == orig_param.getName():
                                    de_parts.append(p.getName() + '=' + quote(self.payload_double_encoded, safe=''))
                                else:
                                    de_parts.append(p.getName() + '=' + quote(p.getValue(), safe='='))
                            de_path_query = url.getPath() + "?" + '&'.join(de_parts)
                            if url.getRef():
                                de_path_query += "#" + url.getRef()
                            requests_to_test.append(build_get_request(de_path_query))

                    # Path segment injection: replace dynamic segments (numeric IDs, UUIDs,
                    # slugs with digits) with the payload one at a time.
                    if self.settings.get("inject_path_segments", True):
                        path_segments, dynamic_segs = self._get_dynamic_path_segments(url.getPath())
                        for seg_idx, _orig_seg in dynamic_segs:
                            variant_segs = path_segments[:]
                            variant_segs[seg_idx] = quote(self.payload, safe='')
                            new_path = '/' + '/'.join(variant_segs)
                            orig_qs = url.getQuery()
                            ps_path_query = new_path + ('?' + orig_qs if orig_qs else '')
                            if url.getRef():
                                ps_path_query += '#' + url.getRef()
                            requests_to_test.append(build_get_request(ps_path_query))

                # Handle POST requests
                elif method == "POST":
                    if self.settings.get("per_param_testing", False) and len(new_parameters) > 1:
                        # Per-param mode: one request per parameter
                        for target_p in new_parameters:
                            per_param_req = request[:]
                            per_param_req = self._helpers.updateParameter(per_param_req, target_p)
                            requests_to_test.append(per_param_req)
                    else:
                        # All-at-once mode (default)
                        modified_request = request[:]
                        if new_parameters:
                            for param in new_parameters:
                                modified_request = self._helpers.updateParameter(modified_request, param)
                        requests_to_test.append(modified_request)

                    # JSON value variants for POST: inject payload into JSON-valued params
                    for orig_param in parameters:
                        if orig_param.getType() == IParameter.PARAM_COOKIE:
                            continue
                        json_injected = self._get_json_injected_value(orig_param.getValue())
                        if json_injected is not None:
                            json_request = request[:]
                            json_param = self._helpers.buildParameter(
                                orig_param.getName(), json_injected, orig_param.getType()
                            )
                            json_request = self._helpers.updateParameter(json_request, json_param)
                            requests_to_test.append(json_request)

                    # Base64 value variants for POST
                    if self.settings.get("inject_base64", True):
                        for orig_param in parameters:
                            if orig_param.getType() == IParameter.PARAM_COOKIE:
                                continue
                            b64_injected = self._get_base64_injected_value(orig_param.getValue())
                            if b64_injected is not None:
                                b64_request = request[:]
                                b64_param = self._helpers.buildParameter(
                                    orig_param.getName(), b64_injected, orig_param.getType()
                                )
                                b64_request = self._helpers.updateParameter(b64_request, b64_param)
                                requests_to_test.append(b64_request)

                    # Double-encoded variants for POST
                    if new_parameters and self.settings.get("double_encode", False):
                        for orig_param in parameters:
                            if orig_param.getType() == IParameter.PARAM_COOKIE:
                                continue
                            de_request = request[:]
                            de_param = self._helpers.buildParameter(
                                orig_param.getName(), self.payload_double_encoded, orig_param.getType()
                            )
                            de_request = self._helpers.updateParameter(de_request, de_param)
                            requests_to_test.append(de_request)

                else:
                    # Unsupported method
                    return
                
                for modified_request in requests_to_test:
                    # Optionally mutate one random cookie
                    if selected_cookie_param is not None:
                        try:
                            cookie_raw = selected_cookie_param.getValue()
                            # Prefer JSON injection, then base64, then plain payload
                            cookie_json = self._get_json_injected_value(cookie_raw)
                            if cookie_json is not None:
                                cookie_value = cookie_json
                            elif self.settings.get("inject_base64", True):
                                cookie_b64 = self._get_base64_injected_value(cookie_raw)
                                cookie_value = cookie_b64 if cookie_b64 is not None else self.payload
                            else:
                                cookie_value = self.payload
                            cookie_param = self._helpers.buildParameter(
                                selected_cookie_param.getName(),
                                cookie_value,
                                IParameter.PARAM_COOKIE
                            )
                            modified_request = self._helpers.updateParameter(modified_request, cookie_param)
                        except Exception:
                            pass

                    # Optionally mutate one random non-essential header
                    if selected_header_name:
                        modified_request = self._replace_header_value(
                            modified_request,
                            selected_header_name,
                            self.payload,
                            messageInfo.getHttpService()
                        )

                    # Inject into a known high-value reflection header (added if absent)
                    if selected_known_header:
                        modified_request = self._add_or_replace_header(
                            modified_request,
                            selected_known_header,
                            self.payload,
                            messageInfo.getHttpService()
                        )

                    # Mark this request as originating from us to avoid re-processing
                    try:
                        if hasattr(self, '_helpers') and self._helpers and modified_request is not None:
                            self.our_requests.add(self._helpers.bytesToString(modified_request))
                    except Exception:
                        pass

                    new_request = self._callbacks.makeHttpRequest(messageInfo.getHttpService(), modified_request)
                    # Analyze the request/response returned by makeHttpRequest
                    try:
                        new_request_data = self._helpers.analyzeRequest(new_request.getHttpService(), new_request.getRequest())
                    except Exception:
                        # Fallback: if analyzeRequest expects raw bytes (older API usage)
                        new_request_data = self._helpers.analyzeRequest(messageInfo.getHttpService(), modified_request)
                        
                    url = new_request_data.getUrl()

                    # Get the response to the new request
                    response = new_request.getResponse()
                    if not response:
                        # Silent fail - no need to spam console for network issues
                        continue
                        
                    response_data = self._helpers.analyzeResponse(response)

                    # Check if response is JSON (matches application/json and +json variants
                    # such as application/ld+json, application/vnd.api+json, application/hal+json)
                    resp_headers_for_ct = response_data.getHeaders()
                    is_json = any(
                        ("application/json" in h.lower().split(":", 1)[1] or "+json" in h.lower().split(":", 1)[1])
                        for h in resp_headers_for_ct
                        if ":" in h and h.lower().split(":", 1)[0].strip() == "content-type"
                    )

                    is_html = any(
                        "text/html" in h.lower().split(":", 1)[1]
                        for h in resp_headers_for_ct
                        if ":" in h and h.lower().split(":", 1)[0].strip() == "content-type"
                    )

                    # Only process HTML responses (and JSON if that setting is on).
                    # Skip JS, CSS, images, and other non-HTML content types.
                    if not is_html and not is_json:
                        continue

                    if is_json and not self.settings.get("detect_json", False):
                        continue

                    # Find response body and check if payload is reflected
                    response_body = response[response_data.getBodyOffset():]
                    if response_data.getBodyOffset() != -1:
                        reflected = []
                        response_body_str = self._helpers.bytesToString(response_body)
                        
                        if "ggg2\"" in response_body_str:
                            reflected.append("\"")
                        if "ggg3>" in response_body_str:
                            reflected.append(">")
                        if "ggg4<" in response_body_str:
                            reflected.append("<")
                        
                        # Check for DOM-based XSS sinks
                        dom_sinks = []
                        
                        # Payload marker anchors used across all sink checks.
                        # We require ggg2" (the literal first marker+char) to avoid matching
                        # any accidental occurrence of the substring "ggg" in page content.
                        P = r'ggg2"'

                        # 1. href attributes - payload must start the attribute value
                        if re.search(r'href\s*=\s*["\']?\s*' + P, response_body_str, re.IGNORECASE):
                            dom_sinks.append("href")
                        
                        # 2. Event handlers - payload must start the handler value
                        event_handlers = ['onclick', 'onload', 'onerror', 'onmouseover', 'onfocus',
                                        'onblur', 'onchange', 'onsubmit', 'onmouseout', 'onkeydown',
                                        'onkeyup', 'onkeypress', 'ondblclick', 'onmousedown', 'onmouseup']
                        for handler in event_handlers:
                            if re.search(handler + r'\s*=\s*["\']?\s*' + P, response_body_str, re.IGNORECASE):
                                dom_sinks.append(handler)
                                break
                        
                        # 3. Dangerous src/data attributes - payload must start the value
                        if re.search(r'<script[^>]+src\s*=\s*["\']?\s*' + P, response_body_str, re.IGNORECASE):
                            dom_sinks.append("script-src")
                        if re.search(r'<iframe[^>]+src\s*=\s*["\']?\s*' + P, response_body_str, re.IGNORECASE):
                            dom_sinks.append("iframe-src")
                        if re.search(r'<embed[^>]+src\s*=\s*["\']?\s*' + P, response_body_str, re.IGNORECASE):
                            dom_sinks.append("embed-src")
                        if re.search(r'<object[^>]+data\s*=\s*["\']?\s*' + P, response_body_str, re.IGNORECASE):
                            dom_sinks.append("object-data")
                        
                        # 4. JavaScript execution contexts - extract script block bodies
                        # and check for actual payload markers, avoiding false positives
                        # from "ggg" appearing anywhere in third-party scripts.
                        script_blocks = re.findall(r'<script[^>]*>(.*?)</script>', response_body_str, re.IGNORECASE | re.DOTALL)
                        if any('ggg2"' in blk or 'ggg3>' in blk for blk in script_blocks):
                            dom_sinks.append("script-context")
                        if re.search(r'\b(eval|setTimeout|setInterval)\s*\(\s*["\']?\s*' + P, response_body_str, re.IGNORECASE):
                            dom_sinks.append("js-exec")
                        
                        # 5. DOM manipulation - directly assigned
                        if re.search(r'\b(innerHTML|outerHTML)\s*=\s*["\']?\s*' + P, response_body_str, re.IGNORECASE):
                            dom_sinks.append("dom-write")
                        if re.search(r'document\.(write|writeln)\s*\(\s*["\']?\s*' + P, response_body_str, re.IGNORECASE):
                            dom_sinks.append("dom-write")
                        
                        # 6. URL/Navigation - directly assigned
                        if re.search(r'\b(location|location\.href)\s*=\s*["\']?\s*' + P, response_body_str, re.IGNORECASE):
                            dom_sinks.append("navigation")
                        if re.search(r'window\.open\s*\(\s*["\']?\s*' + P, response_body_str, re.IGNORECASE):
                            dom_sinks.append("navigation")
                        
                        # 7. Form action - payload must start the value
                        if re.search(r'<form[^>]+action\s*=\s*["\']?\s*' + P, response_body_str, re.IGNORECASE):
                            dom_sinks.append("form-action")
                        
                        tested_url_str = str(new_request_data.getUrl())
                        if reflected or dom_sinks:
                            if tested_url_str in self.reported_urls:
                                continue
                            self.reported_urls.add(tested_url_str)
                            # Format the reflected characters nicely
                            reflected_parts = []
                            if reflected:
                                reflected_parts.append(", ".join(reflected))
                            if dom_sinks:
                                reflected_parts.append("[DOM:" + ",".join(dom_sinks) + "]")

                            if is_json:
                                reflected_parts.append("[JSON]")
                            reflected_str = " ".join(reflected_parts) if reflected_parts else "(detected)"
                            row_tooltip = None
                            if is_json:
                                row_tooltip = "Content-Type: application/json is not vulnerable to XSS. Try to make the website render Content-Type: text/html"
                            self.data.append([self.id, new_request_data.getMethod(), str(new_request_data.getUrl()), reflected_str])
                            self.data_tooltips.append(row_tooltip)
                            self.id = self.id + 1
                            self.data_requests.append(modified_request)
                            self.data_responses.append(response)

                            # Update the JTable after updating the data
                            self.updateTable()
                        
            except Exception as e:
                # Log error quietly without stack trace for common issues
                error_msg = str(e)
                if "java.net" in error_msg or "connection" in error_msg.lower():
                    # Network errors are common, don't spam console
                    pass
                else:
                    # For unexpected errors, provide helpful message
                    print("[isXSS] Unexpected error - " + error_msg)
